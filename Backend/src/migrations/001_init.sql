CREATE TABLE IF NOT EXISTS queue_session (
  id INTEGER PRIMARY KEY,
  current_token INTEGER NOT NULL DEFAULT 0,
  current_serving INTEGER NOT NULL DEFAULT 0
);

INSERT INTO queue_session (id)
