CREATE TABLE IF NOT EXISTS queue_tokens (
  id SERIAL PRIMARY KEY,
  token_number INTEGER NOT NULL,
  status TEXT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
